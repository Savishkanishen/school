    public void tableDetails() {
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
    dtm.setRowCount(0); // Clear existing data
    String id = jTextField1.getText();
    
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/school_data", "root", "");
        
        // Query to get student name and payment amounts
        String query = "SELECT s.name AS student_name, p.amount " +
                      "FROM students s " +
                      "LEFT JOIN payments p ON s.id = p.id " +
                      "WHERE s.id = ?";
        
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, id);
        ResultSet rs = pst.executeQuery();
        
        // Set up table columns if not already set
        if (dtm.getColumnCount() == 0) {
            dtm.addColumn("Student Name");
            dtm.addColumn("Amount Paid");
        }
        
        boolean hasPayments = false;
        String studentName = "";
        
        while (rs.next()) {
            hasPayments = true;
            studentName = rs.getString("student_name");
            dtm.addRow(new Object[]{
                studentName,
                rs.getString("amount")
            });
        }
        
        // If student exists but has no payments
        if (!hasPayments && !studentName.isEmpty()) {
            dtm.addRow(new Object[]{studentName, "No payments recorded"});
        }
        
        rs.close();
        pst.close();
        con.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error loading payment details: " + e.getMessage());
    }

    }
  

