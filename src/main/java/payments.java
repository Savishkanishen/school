    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        tableDetails(); // Refresh table (if needed)

String id = jTextField1.getText().trim();
    
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/school_data", "root", "");
        
        // First check if student exists
        PreparedStatement checkStmt = con.prepareStatement("SELECT * FROM students WHERE id = ?");
        checkStmt.setString(1, id);
        ResultSet rs = checkStmt.executeQuery();
        
        if (rs.next()) {
            // Student exists - populate fields
            jTextField1.setEditable(false);
            jTextField2.setText(rs.getString("name"));
            jTextField3.setText(rs.getString("age"));
            jTextField4.setText(rs.getString("grade"));
            
            // Load payment history into table
            tableDetails();
        } else {
            JOptionPane.showMessageDialog(null, "Student ID does not exist");
        }
        
        rs.close();
        checkStmt.close();
        con.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

